fun stringsFor(code: String): AppStrings = when (code) {
    "es" -> EsStrings
    "fr" -> FrStrings
    "de" -> DeStrings
    "ca" -> CaStrings
    "it" -> ItStrings
    "ja" -> JaStrings
    else -> EnStrings
}
